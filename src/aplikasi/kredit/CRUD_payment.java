    public static final String addMonth(String strDate, int months) {
        Date date;
        try {
            date = dateFormatter.parse(strDate);
        } catch (ParseException e) {
            return null;
        }
        Calendar cal = Calendar.getInstance();
        cal.setTime(date);
        cal.add(Calendar.MONTH, months);
        return dateFormatterByPattern(cal.getTime(), "yyyy-MM-dd");
    }
    
    public static String dateFormatterByPattern(Date date, String pattern) {
        SimpleDateFormat sdf = new SimpleDateFormat(pattern);
        if (date != null) {
            return sdf.format(date);
        } else {
            return sdf.format(new Date());
        }
    }
    
    public Object[] dateCompare (String tanggal_kredit , int angsuran) {
        String tanggal = "";
        String jatuhTempo = "";
        String[] tanggal_jatuh_tempo;
        try {
            LocalDate date_now = LocalDate.now();
            SimpleDateFormat simpleDateFormat = new SimpleDateFormat("yyyy-MM-dd",Locale.ENGLISH);
            Date startDate = simpleDateFormat.parse(tanggal_kredit);
            String add = addMonth(tanggal_kredit,angsuran);
            Date addMonths = simpleDateFormat.parse(add); // 2022-07-22
            Date endDate = simpleDateFormat.parse(date_now.toString()); // 2022-07-25
            long difference = 0;
            int daysBetween = 0;
            if (addMonths.getTime() < endDate.getTime()) {
                difference = endDate.getTime() - addMonths.getTime();
                daysBetween = (int) (difference / (1000*60*60*24));
            } else {
                difference = 0;
                daysBetween = 0;
            }
            jatuhTempo = add;
            tanggal = String.valueOf(daysBetween);
        } catch (Exception e) {
        }
        return new Object[]{tanggal,jatuhTempo};
    }
    
