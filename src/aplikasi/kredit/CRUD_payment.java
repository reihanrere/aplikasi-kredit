    private void show_data(){
//         membuat tampilan model tabel
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id");
        model.addColumn("No Kontrak");
        model.addColumn("Angsuran");
        model.addColumn("Jml Angsuran");
        model.addColumn("Denda");
        model.addColumn("Pembayaran");
        
        //menampilkan data database kedalam tabel
        try {
            String sql = "SELECT pembayaran.id_pembayaran, pembayaran.no_kontrak, pembayaran.angsuran, pembayaran.jumlah, pembayaran.denda, pembayaran.total FROM pembayaran";
            java.sql.Connection conn=(Connection)config.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{res.getString("id_pembayaran"),res.getString("no_kontrak"),res.getString("angsuran"),res.getString("jumlah"),res.getString("denda"),res.getString("total")});
            }
            table_pembayaran.setModel(model);
            System.out.print("Berhasil Nampil Data");
        } catch (SQLException e) {
            System.out.print("Gagal Nampil Data");
        }
    }
    
