    public static final int angsuranKe(String no_kontrak) {
        int angsuranText = 0;
        try {
            String sql = "SELECT * FROM pembayaran WHERE no_kontrak = '"+no_kontrak+"' ORDER BY angsuran";
            java.sql.Connection conn=(Connection)config.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while (res.next()) {                
                String angsuran = res.getString("angsuran");
                angsuranText = Integer.parseInt(angsuran);
            }
        } catch (Exception e) {
        }
        return angsuranText;
    } 

